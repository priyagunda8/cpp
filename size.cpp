#include<iostream>
using namespace std;
int main()
{
    int a;
    char c;
    float f;
    double d;
    cout<<"size of char: "<<sizeof (c)<< " byte" <<endl;
    cout<<" size of int: "<<sizeof (a)<<" bytes"<<endl;
    cout<<"size of float: "<<sizeof (f)<<" bytes"<<endl;
    cout<<"size of double: "<<sizeof (d)<<" bytes"<<endl;
    return 0;
}
